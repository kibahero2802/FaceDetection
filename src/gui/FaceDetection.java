    private DaemonThread myThread = null;
    int count = 0;
    VideoCapture webSource = null;
    Mat frame = new Mat();
    MatOfByte mem = new MatOfByte();
    CascadeClassifier faceDetector = new CascadeClassifier(FaceDetection.class.getResource("haarcascade_frontalface_alt.xml").getPath().substring(1));
    CascadeClassifier noseDetector = new CascadeClassifier(FaceDetection.class.getResource("haarcascade_nose.xml").getPath().substring(1));
    CascadeClassifier eyeDetector = new CascadeClassifier(FaceDetection.class.getResource("haarcascade_eye.xml").getPath().substring(1));
    CascadeClassifier mouthDetector = new CascadeClassifier(FaceDetection.class.getResource("haarcascade_mouth.xml").getPath().substring(1));
